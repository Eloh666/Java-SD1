public class Country 
{
    public String name;
    public String region;
    public int area;
    public int pop;
    public long gdp;

    Country(String name, String region, int area, int pop, long gdp)
    {
        this.name = name;
        this.region = region;
        this.area = area;
        this.pop = pop;
        this.gdp = gdp;
    }
}